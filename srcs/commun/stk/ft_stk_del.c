#include <push_swap.h>

void		ft_stk_del(t_stk **stk)
{
	t_stk	*free_stk;

	if (stk == NULL || *stk == NULL)
		return ;
	free_stk = *stk;
	*stk = (*stk)->next;
	free(free_stk);
	ft_stk_del(stk);
}
