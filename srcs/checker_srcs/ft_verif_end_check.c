#include <push_swap.h>

void		ft_verif_end_check(t_a *a)
{
	t_stk	*stk;

	if (a->stk_b != NULL)
	{
		ft_printf("KO\n");
		return ;
	}
	stk = a->stk_a;
	while (stk->next)
	{
		if (stk->nb >= stk->next->nb)
		{
			ft_printf("KO\n");
			return ;
		}
		stk = stk->next;
	}
	ft_printf("OK\n");
}
